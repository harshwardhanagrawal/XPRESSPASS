    private int validateInput() {
        hodName = txtName.getText().trim();
        hodEmpId = txtEmpIdReg.getText().trim();
        hodBranch = String.valueOf(jcbBranch.getSelectedItem());
        hodCollege = String.valueOf(jcbCollege.getSelectedItem());
        hodEmail = txtEmail.getText().trim();
        hodPwd = String.valueOf(txtPwdReg.getPassword());
        hodCpwd = String.valueOf(txtConfPwdReg.getPassword());
        if (hodName.isEmpty() || hodEmpId.isEmpty() || hodBranch.isEmpty() || hodCollege.isEmpty() || hodEmail.isEmpty() || hodPwd.isEmpty()
                || hodCpwd.isEmpty()) {
            return 0;
        }
        if (hodEmail.contains("@gmail.com") == false) {
            return -1;
        }
        if (hodPwd.equals(hodCpwd) == false) {
            return -2;
        }
        return 1;
    }

