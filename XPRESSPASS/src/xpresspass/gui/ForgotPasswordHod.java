    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        boolean isValidInput = validateInputs();
        if (isValidInput == false) {
            JOptionPane.showMessageDialog(null, "Fields can not be left blank!", "Oops!", JOptionPane.ERROR_MESSAGE);
            return;
        } else if ((new String(pwd)).equals(new String(cPwd)) == false) {
            JOptionPane.showMessageDialog(null, "Password and confirm password do not match", "Oops!", JOptionPane.ERROR_MESSAGE);
            txtPwd.setText("");
            txtCpwd.setText("");
            txtPwd.requestFocus();
            return;
        }
        try {
            String password= String.valueOf(pwd);
            HodPojo hPojo = new HodPojo();
            hPojo.setHodEmpId(userId);
            hPojo.setHodPwd(password);
            if (HodDAO.updatePassword(hPojo)) {
                JOptionPane.showMessageDialog(null, "Password changed successfully", "Success!", JOptionPane.INFORMATION_MESSAGE, icon2);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Password can not be changed", "ERROR!", JOptionPane.ERROR_MESSAGE);
            }
            txtEmpId.setText("");
            txtPwd.setText("");
            txtCpwd.setText("");

        } catch (SQLException e) {
            e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Some exception occoured", "ERROR!", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

