    private boolean validateInputs() {
        userId = txtEmpId.getText().trim();
        pwd = txtPwd.getPassword();
        cPwd = txtCpwd.getPassword();

        if (userId.isEmpty() || pwd.length == 0 || cPwd.length == 0) {
            return false;
        }

        return true;
    }

